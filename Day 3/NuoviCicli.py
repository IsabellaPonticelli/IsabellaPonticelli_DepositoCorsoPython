#ciclo while 

conteggio = 0

while conteggio < 5:
    print(conteggio)
    conteggio +=1

# ciclo for
numeri = [1,2,3,4,5]
          
for numero in numeri:
    print(numero)
    
#range

for i in range(2,8): #per ogni numero nel range che va da 2 a 8(non compreso)
    print(i)
    
for i in range(1,10,2): #per ogni numero nel range che va da 1 a 10, ogni due numeri
    print(i)
    
